/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_ADC1_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */
	HAL_UART_Receive_IT(&huart1, &rx_char, 1);
	HAL_TIM_PWM_Start(&htim2,TIM_CHANNEL_4); // ENA
	HAL_TIM_PWM_Start(&htim3,TIM_CHANNEL_1); // ENB
	HAL_TIM_PWM_Start(&htim4,TIM_CHANNEL_4); // SERVO	
	HAL_ADC_Start(&hadc1);
	
	htim2.Instance->CCR4 = (uint32_t)(speed*65535.0f); // left = 3/2 right
	htim3.Instance->CCR1 = (uint32_t)(speed*(0.6667f)*65535.0f);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  { 	
		while (rx_char == TUDONG)
		{
			front_d = hcsr04_read(SERVO_MID); 	
			if (front_d > 110)
			{
				robot_mover(FORWARD);
			}
			else
			{
				robot_mover(BACKWARD);
				HAL_Delay(300);
				robot_mover(STOP);
				left_d  = hcsr04_read(SERVO_LEFT);
				right_d = hcsr04_read(SERVO_RIGHT);
				//HAL_Delay(200);
				if (left_d > right_d)
				{
					robot_mover(LEFT_BACK); 
					front_d = hcsr04_read(SERVO_MID); 	
					HAL_Delay(300);
				}
				else 
				{
					robot_mover(RIGHT_BACK); // right back
					front_d = hcsr04_read(SERVO_MID); 	
					HAL_Delay(300);
				}
			}
		}
		if (rx_char == NO_VOICE)
			robot_mover(STOP);
    if (rx_char == TOI)
			robot_mover(FORWARD);
		if (rx_char == LUI)
			robot_mover(BACKWARD);
		if (rx_char == TRAI)
		{
			robot_mover(LEFT_FORWARD);
			HAL_Delay(1000);
			rx_char = NO_VOICE;
		}
		if (rx_char == PHAI)
		{
			robot_mover(RIGHT_FORWARD);	
			HAL_Delay(1000);
			rx_char = NO_VOICE;
		}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

